#include<iostream>
#include<conio.h>

using namespace std;
int main(){

int x=3,y=2;

x+=y;   // X=X+Y=3+2=5
cout<<x<<endl;

x-=y;   // X=X-Y=5-2=3
cout<<x<<endl;

x*=y;  // X=X*Y=3*2=6
cout<<x<<endl;

x/=y;  // X=X/Y=6/2=3
cout<<x<<endl;

x%=y;  // X=X%Y=3%2=1
cout<<x<<endl;



getch();
}


